# Formatear numeros

# format()

entero = 42
decimal = 31415.9265
porcentaje = 0.19

print(format(entero, 'X')) #Pasa el valor entereo a Hexadecimal 2A
print(format(decimal, 'e')) #Pasa a notacion cientifica
print(format(decimal, '.2f')) # toma solo dos lugares depues de la coma
print(format(porcentaje, '%')) # muestra el resultado en porcntaje
